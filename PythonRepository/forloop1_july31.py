#for is a Definite Loop which runs for a finite period of time, i is the iteration variable taking the values in the list
#since it is an integer we are iterating through, we are naming it i, we can give it any name though
for i in [5,4,3,2,1]:
    print(i)
print('done')
friends=['gopi','prabhash','naveen']
for friend in friends:#friend is an string iteration variable
    print('Happy New Year',friend)
print('done')
